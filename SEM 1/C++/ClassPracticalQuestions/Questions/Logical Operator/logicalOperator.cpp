#include <iostream>
using namespace std;
int main()
{
    cout << "Use of logical operators" << endl;
    cout << "8&&-2 is: " << (8 && -2) << endl;
    cout << "9&&0 is: " << (9 && 0) << endl;
    cout << "0&&4 is: " << (0 && 4) << endl;
    cout << "7||0 is: " << (7 || 0) << endl;
    cout << "0||0 is: " << (0 || 0) << endl;
    cout << "!2&&!0 is: " << (!2 && !0) << endl;
    system("pause");
    return 0;
}
