#include <iostream>
using namespace std;
int main()
{
    int a = 5, b = 4;
    cout << "a is 5 and b is 4" << endl;
    cout << "a>b is " << (a > b) << endl;
    cout << "a<b is " << (a < b) << endl;
    cout << "a<=b is " << (a <= b) << endl;
    cout << "a>=b is " << (a >= b) << endl;
    system("pause");
    return 0;
}
